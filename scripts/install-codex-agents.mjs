#!/usr/bin/env node

import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = path.join(repoRoot, ".codex", "agents");
const destinationRoot = path.join(os.homedir(), ".codex", "agents");
const generatedMarker = "# Generated by Proteus";

if (!fs.existsSync(sourceRoot)) throw new Error(`missing generated Codex agents: ${sourceRoot}`);
fs.mkdirSync(destinationRoot, { recursive: true });

const installed = [];
for (const filename of fs.readdirSync(sourceRoot).filter((name) => name.endsWith(".toml")).sort()) {
  const source = path.join(sourceRoot, filename);
  const destination = path.join(destinationRoot, filename);
  prepareDestination(source, destination);
  try {
    fs.symlinkSync(source, destination, "file");
  } catch (error) {
    if (process.platform !== "win32" || !["EPERM", "EACCES", "UNKNOWN"].includes(error?.code)) throw error;
    fs.copyFileSync(source, destination);
  }
  installed.push(destination);
}

console.log("Installed Proteus Codex agents:");
for (const destination of installed) console.log(`- ${destination}`);
console.log("Start a new Codex session before using the native Proteus agent types.");

function prepareDestination(source, destination) {
  if (!fs.existsSync(destination)) return;
  const stat = fs.lstatSync(destination);
  if (stat.isSymbolicLink()) {
    fs.unlinkSync(destination);
    return;
  }
  if (stat.isFile()) {
    const existing = fs.readFileSync(destination, "utf8");
    const generated = fs.readFileSync(source, "utf8");
    if (existing === generated || existing.startsWith(generatedMarker)) {
      fs.unlinkSync(destination);
      return;
    }
  }
  throw new Error(
    `refusing to replace non-Proteus agent profile: ${destination}\n` +
      "Move it aside or delete it before running this installer."
  );
}
