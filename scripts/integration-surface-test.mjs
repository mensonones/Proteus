import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const codexMarketplacePath = path.join(repoRoot, ".agents", "plugins", "marketplace.json");
const codexMarketplace = readJson(codexMarketplacePath);
const codexEntry = codexMarketplace.plugins?.find((entry) => entry.name === "proteus");
assert(codexEntry, "Codex marketplace is missing the Proteus entry");
assert(codexEntry.policy?.installation, "Codex marketplace entry is missing installation policy");
assert(codexEntry.policy?.authentication, "Codex marketplace entry is missing authentication policy");

const pluginRoot = path.resolve(repoRoot, String(codexEntry.source?.path ?? codexEntry.source));
const codexManifest = readJson(path.join(pluginRoot, ".codex-plugin", "plugin.json"));
assert(codexManifest.name === "proteus", "Codex plugin name must be proteus");
assertFile(path.resolve(pluginRoot, codexManifest.skills));
const codexMcpConfigPath = path.resolve(pluginRoot, codexManifest.mcpServers);
assertFile(codexMcpConfigPath);
const codexMcpConfig = readJson(codexMcpConfigPath);
assert(codexMcpConfig.mcpServers?.proteus?.cwd === ".", "Codex plugin MCP must resolve relative paths from the plugin root");
assertSkill(path.join(pluginRoot, "skills", "proteus", "SKILL.md"), "proteus");
assertSkill(path.join(pluginRoot, "skills", "continuous-vuln-research", "SKILL.md"), "continuous-vuln-research");
const roleFiles = fs.readdirSync(path.join(pluginRoot, "agents")).filter((name) => name.startsWith("proteus-") && name.endsWith(".md")).sort();
const codexAgentFiles = fs.readdirSync(path.join(repoRoot, ".codex", "agents")).filter((name) => name.endsWith(".toml")).sort();
const expectedRoleFiles = [
  "proteus-argus.md",
  "proteus-artificer.md",
  "proteus-atlas.md",
  "proteus-chaos.md",
  "proteus-cicada.md",
  "proteus-libris.md",
  "proteus-loom.md",
  "proteus-mimic.md",
  "proteus-skeptic.md"
].sort();
assert(JSON.stringify(roleFiles) === JSON.stringify(expectedRoleFiles), `Proteus role contracts differ from the expected set: ${roleFiles.join(", ")}`);
assert(codexAgentFiles.length === roleFiles.length, "Codex native agent count does not match role contracts");
for (const roleFile of roleFiles) {
  const roleName = path.basename(roleFile, ".md");
  const agentPath = path.join(repoRoot, ".codex", "agents", `${roleName}.toml`);
  assertFile(agentPath);
  const agent = fs.readFileSync(agentPath, "utf8");
  assert(agent.startsWith("# Generated by Proteus"), `${agentPath} is not generated from the canonical role contract`);
  assert(agent.includes(`name = "${roleName}"`), `${agentPath} has the wrong native agent name`);
  assert(agent.includes("developer_instructions = '''"), `${agentPath} is missing developer instructions`);
  assert(agent.includes('sandbox_mode = "read-only"') || agent.includes('sandbox_mode = "workspace-write"'), `${agentPath} has no sandbox profile`);
  if (roleName === "proteus-atlas") {
    assert(agent.includes('sandbox_mode = "read-only"'), "Atlas must remain read-only");
    assert(agent.includes("do not perform a broad"), "Atlas must prohibit broad vulnerability review");
  }
}

const claudeMarketplace = readJson(path.join(repoRoot, ".claude-plugin", "marketplace.json"));
const claudeEntry = claudeMarketplace.plugins?.find((entry) => entry.name === "proteus");
assert(claudeEntry, "Claude marketplace is missing the Proteus entry");
assertFile(path.join(pluginRoot, ".claude-plugin", "plugin.json"));
assertFile(path.join(pluginRoot, "commands", "proteus.md"));

const openCodeConfig = readJson(path.join(repoRoot, "opencode.json"));
assert(openCodeConfig.command?.proteus, "OpenCode /proteus command is missing");
assert(openCodeConfig.skills?.paths?.includes(".opencode/skills"), "OpenCode skill path is missing");
assertFile(path.join(repoRoot, ".opencode", "skills", "proteus", "SKILL.md"));
assertFile(path.join(repoRoot, ".opencode", "skills", "proteus-mobile-reversing", "scripts", "extract_mobile_artifacts.py"));
const openCodeAgents = fs.readdirSync(path.join(repoRoot, ".opencode", "agents")).filter((name) => name.startsWith("proteus-") && name.endsWith(".md"));
assert(openCodeAgents.length === roleFiles.length, "OpenCode agent count does not match canonical role contracts");

const packageJson = readJson(path.join(repoRoot, "package.json"));
assert(packageJson.files?.includes(".codex/"), "npm package does not include native Codex agents");
assert(packageJson.bin?.["proteus-install-codex-agents"], "npm package does not expose the Codex agent installer");

console.log("Proteus integration surface test passed: Codex, Claude Code, and OpenCode");

function readJson(filePath) {
  assertFile(filePath);
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function assertFile(filePath) {
  assert(fs.existsSync(filePath), `Missing integration file: ${filePath}`);
}

function assertSkill(filePath, expectedName) {
  assertFile(filePath);
  const content = fs.readFileSync(filePath, "utf8");
  assert(content.includes(`\nname: ${expectedName}\n`), `Skill ${filePath} must declare name: ${expectedName}`);
}

function assert(condition, message) {
  if (!condition) throw new Error(message);
}
