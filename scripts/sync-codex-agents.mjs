import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = path.join(repoRoot, "plugins", "proteus", "agents");
const destinationRoot = path.join(repoRoot, ".codex", "agents");

const roleSettings = {
  "proteus-atlas": { sandbox: "read-only", reasoning: "high", nicknames: ["Atlas"] },
  "proteus-argus": { sandbox: "read-only", reasoning: "high", nicknames: ["Argus"] },
  "proteus-loom": { sandbox: "read-only", reasoning: "high", nicknames: ["Loom"] },
  "proteus-chaos": { sandbox: "workspace-write", reasoning: "high", nicknames: ["Chaos"] },
  "proteus-libris": { sandbox: "read-only", reasoning: "medium", nicknames: ["Libris"] },
  "proteus-mimic": { sandbox: "workspace-write", reasoning: "high", nicknames: ["Mimic"] },
  "proteus-artificer": { sandbox: "workspace-write", reasoning: "high", nicknames: ["Artificer"] },
  "proteus-skeptic": { sandbox: "read-only", reasoning: "high", nicknames: ["Skeptic"] },
  "proteus-cicada": { sandbox: "workspace-write", reasoning: "high", nicknames: ["Cicada"] }
};

fs.rmSync(destinationRoot, { recursive: true, force: true });
fs.mkdirSync(destinationRoot, { recursive: true });

for (const [roleName, settings] of Object.entries(roleSettings)) {
  const sourcePath = path.join(sourceRoot, `${roleName}.md`);
  if (!fs.existsSync(sourcePath)) throw new Error(`missing role contract: ${sourcePath}`);
  const { name, description, instructions } = parseRole(fs.readFileSync(sourcePath, "utf8"));
  if (name !== roleName) throw new Error(`role name mismatch in ${sourcePath}: ${name}`);
  const output = [
    "# Generated by Proteus from plugins/proteus/agents. Do not edit directly.",
    `name = ${tomlString(name)}`,
    `description = ${tomlString(description)}`,
    `nickname_candidates = [${settings.nicknames.map(tomlString).join(", ")}]`,
    `model_reasoning_effort = ${tomlString(settings.reasoning)}`,
    `sandbox_mode = ${tomlString(settings.sandbox)}`,
    "developer_instructions = '''",
    instructions.trim(),
    "'''",
    ""
  ].join("\n");
  fs.writeFileSync(path.join(destinationRoot, `${roleName}.toml`), output);
}

console.log(`Synchronized ${Object.keys(roleSettings).length} Codex agent profiles to ${destinationRoot}`);

function parseRole(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) throw new Error("role contract is missing YAML-style front matter");
  const fields = {};
  for (const line of match[1].split(/\r?\n/)) {
    const separator = line.indexOf(":");
    if (separator === -1) continue;
    fields[line.slice(0, separator).trim()] = line.slice(separator + 1).trim();
  }
  if (!fields.name || !fields.description) throw new Error("role contract requires name and description");
  return { name: fields.name, description: fields.description, instructions: match[2] };
}

function tomlString(value) {
  return JSON.stringify(String(value));
}
